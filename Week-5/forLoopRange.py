def main():
    # Example 1: Range with one argument
    print("Example 1")
    for x in range(5):
        print(x)
    
    print()

    # Example 2: Range with one argument
    print("Exaple 2")
    for x in range(10):
        print(x, end=f", ")

    print()
    print()

    # Example 3: Range with two arguments
    print("Exaple 3")
    for x in range(6, 10):
        print(x, end=f", ")

    print()
    print()

if __name__ == "__main__":
    main()